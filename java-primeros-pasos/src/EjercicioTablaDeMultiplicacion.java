
public class EjercicioTablaDeMultiplicacion {
	public static void main(String[] args) {
		for (int contador = 0; contador <= 10 ; contador ++) {
			for (int mult = 0; mult <=10 ; mult ++) {
				System.out.print(contador * mult);
				System.out.print(" ");
			}
			System.out.println();
		}
	}
}
